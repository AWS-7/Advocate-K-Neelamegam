import fs from "fs/promises";
import path from "path";
import { fileURLToPath } from "url";
import sharp from "sharp";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, "..");
const imagesDir = path.join(root, "public", "images");

const SOURCE_FILE = "Screenshot 2026-06-17 182944.png";
const PORTRAIT_FILE = "advocate-k-neelamegam-high-court-portrait.webp";
const OG_FILE = "og-image.webp";
const PORTRAIT_MAX_WIDTH = 960;
const WEBP_QUALITY = 86;

async function main() {
  const inputPath = path.join(root, SOURCE_FILE);
  const portraitPath = path.join(imagesDir, PORTRAIT_FILE);
  const ogPath = path.join(imagesDir, OG_FILE);

  await fs.access(inputPath);

  const portraitMeta = await sharp(inputPath).rotate().metadata();
  const portraitWidth = Math.min(portraitMeta.width ?? PORTRAIT_MAX_WIDTH, PORTRAIT_MAX_WIDTH);
  const portraitHeight = portraitMeta.height
    ? Math.round((portraitWidth / (portraitMeta.width ?? 1)) * portraitMeta.height)
    : 1200;

  const portraitBuffer = await sharp(inputPath)
    .rotate()
    .resize({ width: portraitWidth, withoutEnlargement: true })
    .webp({ quality: WEBP_QUALITY, effort: 4 })
    .toBuffer();

  await fs.writeFile(portraitPath, portraitBuffer);

  const fittedPortrait = await sharp(portraitBuffer)
    .resize(520, 620, { fit: "cover", position: "centre" })
    .toBuffer();

  await sharp({
    create: {
      width: 1200,
      height: 630,
      channels: 3,
      background: { r: 11, g: 29, b: 58 },
    },
  })
    .composite([{ input: fittedPortrait, left: 640, top: 5 }])
    .webp({ quality: WEBP_QUALITY })
    .toFile(ogPath);

  const tsContent = `/* Auto-generated by scripts/optimize-seo-images.mjs — do not edit manually */
export const advocatePortraitImage = {
  path: "/images/${PORTRAIT_FILE}",
  alt: "Advocate K. Neelamegam — Lumbini Law Associates, High Court Advocate Madurai",
  width: ${portraitWidth},
  height: ${portraitHeight},
} as const;

export const ogShareImage = {
  path: "/images/${OG_FILE}",
  alt: "Lumbini Law Associates — Advocate K. Neelamegam | High Court Lawyer Madurai",
  width: 1200,
  height: 630,
} as const;
`;

  await fs.writeFile(path.join(root, "src", "lib", "seo-images.ts"), tsContent, "utf8");

  const portraitStat = await fs.stat(portraitPath);
  const ogStat = await fs.stat(ogPath);

  console.log(`✓ ${PORTRAIT_FILE} — ${(portraitStat.size / 1024).toFixed(1)} KB (${portraitWidth}x${portraitHeight})`);
  console.log(`✓ ${OG_FILE} — ${(ogStat.size / 1024).toFixed(1)} KB`);
  console.log("✓ src/lib/seo-images.ts updated");
}

main().catch((error) => {
  console.error(error);
  process.exit(1);
});
